<p>Online chat may refer to any kind of communication over the Internet
that offers a real-time transmission of text messages from sender to receiver.</p>
<p>Chat messages are generally short in order to enable other participants to 
respond quickly. Thereby, a feeling similar to a spoken conversation is created,
 which distinguishes chatting from other text-based online communication forms 
 such as Internet forums and email.</p>
<p> Online chat may address point-to-point 
 communications as well as multicast communications from one sender to many 
 receivers and voice and video chat, or may be a feature of a web conferencing service.</p>
